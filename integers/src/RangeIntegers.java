public class RangeIntegers
{
    public static void main(String[] args)
    {
        int valInt=2147483647;
        long valLn=9223372036854775807L;
        short valSrt=32767;
        byte valBte=127;

        System.out.println("Integer: "+valInt);
        System.out.println("Long: "+valLn);
        System.out.println("Short: "+valSrt);
        System.out.println("valBte: "+valBte);
    }
}
